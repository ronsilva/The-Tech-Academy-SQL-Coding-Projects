CREATE PROC LP.uspFindLostTribe
AS
/*1.) How many copies of the book titled "The Lost Tribe" are owned by the library branch whose name is "Sharpstown"?*/
SELECT Number_Of_Copies '# Of Coppies', BranchName 'Library'
FROM BOOK_COPIES bc
INNER JOIN LIBRARY_BRANCH lb ON lb.BranchID = bc.BranchID
INNER JOIN BOOKS bk ON bk.BookID = bc.BookID
WHERE Title = 'The Lost Tribe' and BranchName = 'Sharpstown Library'
GO

