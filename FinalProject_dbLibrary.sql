CREATE PROC LP.uspBorrowersNoBook
AS
/*3.) Retrieve the names of all borrowers who do not have any books checked out.*/  
SELECT DISTINCT Name 'Borrowers W/O Books'
FROM BORROWER br 
LEFT JOIN BOOK_LOANS bl ON br.CardNo = bl.CardNo
WHERE br.CardNo not in (SELECT CardNo FROM BOOK_LOANS)
GO

CREATE PROC LP.uspSharptownBorrowerDueToday
AS
/*4.) For each book that is loaned out from the "Sharpstown" branch and whose DueDate is today, retrieve the book title, the borrower's name, and the borrower's address.*/  
SELECT DISTINCT Title, Name 'Borrowe''s Name', br.Address
FROM BOOKS bk 
INNER JOIN BOOK_LOANS bl ON bk.BookID = bl.BookID
INNER JOIN LIBRARY_BRANCH lb ON lb.BranchID = bl.BranchID
INNER JOIN BORROWER br ON br.CardNo = bl.CardNo
WHERE DateDue = CONVERT (date, GETDATE())
GO

CREATE PROC LP.uspTotalNumOfBooksLoanedByBranch
AS
/*5.) For each library branch, retrieve the branch name and the total number of books loaned out from that branch.*/
SELECT BranchName 'Branch Name', COUNT(bl.BranchID) 'Total # of Books loaned' 
FROM LIBRARY_BRANCH lb
LEFT JOIN BOOK_LOANS bl ON lb.BranchID = bl.BranchID 
GROUP BY BranchName
ORDER BY COUNT(bl.BranchID) DESC
GO

CREATE PROC LP.uspBorrowersWithMoreThan5Books
AS
/*6.) Retrieve the names, addresses, and the number of books checked out for all borrowers who have more than five books checked out.*/  
SELECT Name, br.Address, COUNT(BookID) 'Total # of Books loaned'
FROM BORROWER br 
INNER JOIN BOOK_LOANS bl ON br.CardNo = bl.CardNo
GROUP BY Name, br.Address
HAVING Count(BookID) >= 5
GO

