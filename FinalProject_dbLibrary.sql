CREATE DATABASE db_Library
GO

USE db_Library
GO

/*Main Tables*/
CREATE TABLE LIBRARY_BRANCH(
 BranchID INT Primary Key Not Null IDENTITY(1,1),
 BranchName varchar(50) Not Null,
 Address varchar(50) Not Null
)
GO

CREATE TABLE PUBLISHER(
 PublisherName varchar(50) Primary Key Not Null,
 Address varchar(50) Not Null,
 Phone varchar(50) Not Null
)
GO

CREATE TABLE BORROWER(
 CardNo INT Primary Key Not Null IDENTITY(10000,1),
 Name varchar(50) Not Null,
 Address varchar(50) Not Null,
 Phone varchar(50) Null
)
GO

/*Dependent Tables*/
CREATE TABLE BOOKS(
 BookID INT Primary Key Not Null IDENTITY(100000,1),
 Title varchar(100) Not Null,
 PublisherName varchar(50) CONSTRAINT fk_PublisherName FOREIGN KEY REFERENCES PUBLISHER(PublisherName) ON UPDATE CASCADE ON DELETE CASCADE
)
GO

CREATE TABLE BOOK_COPIES(
 BookID INT CONSTRAINT fky_BookID FOREIGN KEY REFERENCES BOOKS(BookID) ON UPDATE CASCADE ON DELETE CASCADE,
 BranchID INT CONSTRAINT fky_BranchID FOREIGN KEY REFERENCES LIBRARY_BRANCH(BranchID) ON UPDATE CASCADE ON DELETE CASCADE,
 Number_Of_Copies INT
)
GO

CREATE TABLE BOOK_LOANS(
 BookID INT CONSTRAINT fnk_BookID FOREIGN KEY REFERENCES BOOKS(BookID) ON UPDATE CASCADE ON DELETE CASCADE,
 BranchID INT CONSTRAINT fnk_BranchID FOREIGN KEY REFERENCES LIBRARY_BRANCH(BranchID) ON UPDATE CASCADE ON DELETE CASCADE,
 CardNo INT CONSTRAINT fnk_CardNo FOREIGN KEY REFERENCES BORROWER(CardNo) ON UPDATE CASCADE ON DELETE CASCADE,
 DateOut Date,
 DateDue Date
)
GO

CREATE TABLE BOOK_AUTHORS(
 BookID INT CONSTRAINT fk_BookID FOREIGN KEY REFERENCES BOOKS(BookID) ON UPDATE CASCADE ON DELETE CASCADE,
 AuthorName varchar(50)
)
GO

